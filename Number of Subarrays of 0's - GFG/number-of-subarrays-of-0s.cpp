long long int no_of_subarrays(int n, vector<int> &arr) {
    // Write your code here.
    
    long long int i=0,z=0,res=0;
    while(i<n){
        if(arr[i++]==0)
        {
            z++;
        }
        else {
        z=0;
        }
        res+=z;
    }
    return res;
}

